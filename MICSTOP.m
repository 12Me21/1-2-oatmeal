#+TITLE
Stop recording audio.
The mic must be enabled with [[XON MIC]] first.
** Syntax
``` sbsyntax
MICSTOP
```
** Possible Errors
|*Error|Cause|
|`Mic is not available`|Mic is not enabled (Use [[XON MIC]] first)|
|`Illegal function call`|Wrong number of arguments|
** See Also
